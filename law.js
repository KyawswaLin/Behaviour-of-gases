let count=0;
function showMyan(){
    let Translate=document.getElementById("accordion-content-mya");  
      if(count==0){
      Translate.style.display="block";
      Translate.style.opacity="1";
      count=1;
    }else{
      Translate.style.display="none";
      Translate.style.opacity="0";
      count=0;
    }
  }

